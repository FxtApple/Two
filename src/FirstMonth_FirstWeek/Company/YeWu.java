package FirstMonth_FirstWeek.Company;

public class YeWu {
    int yeWuID;
    String name;
    public int time;
    public int yuanGongMount;
    public boolean isDaBian;

    public int getYeWuID() {
        return yeWuID;
    }

    public void setYeWuID(int yeWuID) {
        this.yeWuID = yeWuID;
    }

    public String getName() {
        return name;
    }

    public void setName(String namr) {
        this.name = namr;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public int getYuanGongMount() {
        return yuanGongMount;
    }

    public void setYuanGongMount(int yuanGongMount) {
        this.yuanGongMount = yuanGongMount;
    }

    public boolean isDaBian() {
        return isDaBian;
    }

    public void setDaBian(boolean daBian) {
        isDaBian = daBian;
    }
}
